public class DataType5 {

	public static void main(String[] args) {
		int app, pear, orange;
		app = 5;
		pear = 7;
		orange = 5;
		
		int total = app + pear + orange;
		
		float avg = total / 24.0f;
		
		System.out.println("총 생산 수 : " + total);
		System.out.println("시간당 생산 갯수 : " + avg);
		
	
	}

}
